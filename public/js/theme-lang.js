const injectControls = () => {
  const makeCluster = (compact) => {
    const cluster = document.createElement('div');
    cluster.className = compact ? 'sidebar-controls' : 'controls-cluster';
    cluster.style.cssText = compact
      ? 'display:flex;gap:6px;padding:0 0.75rem 0.75rem;'
      : 'display:flex;gap:6px;align-items:center;';

    const themeBtn = document.createElement('button');
    themeBtn.className = 'theme-toggle-btn';
    themeBtn.title = 'Toggle theme';
    themeBtn.addEventListener('click', () => ThemeManager.toggle());

    const langBtn = document.createElement('button');
    langBtn.className = 'lang-switcher-btn';
    langBtn.title = 'Switch language';
    langBtn.addEventListener('click', () => LangManager.toggle());

    cluster.appendChild(themeBtn);
    cluster.appendChild(langBtn);
    return cluster;
  };

  // Sidebar footer — inject before last nav-item
  // const sidebarFooter = document.querySelector('.sidebar-footer');
  // if (sidebarFooter && !sidebarFooter.querySelector('.sidebar-controls')) {
  //   const logout = sidebarFooter.querySelector('.nav-item:last-child');
  //   sidebarFooter.insertBefore(makeCluster(true), logout || null);
  // }

  // Header actions — inject once
  const headerActions = document.querySelector('.header-actions');
  if (headerActions && !headerActions.querySelector('.controls-cluster')) {
    headerActions.prepend(makeCluster(false));
  }

  // Public nav cta — inject once
  const navCta = document.querySelector('.nav-cta');
  if (navCta && !navCta.querySelector('.controls-cluster')) {
    navCta.appendChild(makeCluster(false));
  }

  // Refresh button labels after injecting
  ThemeManager.updateButtons(ThemeManager.get());
  LangManager.updateButtons(LangManager.get());
};

