// THEME SYSTEM
// ═══════════════════════════════════════════════════════════════
const ThemeManager = {
  KEY: 'eduadapt_theme',
  get()   { return localStorage.getItem(this.KEY) || 'light'; },
  set(t)  { localStorage.setItem(this.KEY, t); this.apply(t); this.updateButtons(t); },
  apply(t){ document.body.classList.toggle('dark-mode', t === 'dark'); },
  toggle(){ this.set(this.get() === 'dark' ? 'light' : 'dark'); },
  updateButtons(t) {
    const lang = LangManager.get();
    document.querySelectorAll('.theme-toggle-btn').forEach(btn => {
      btn.textContent = t === 'dark'
        ? (TRANSLATIONS[lang]['theme.light'] || '☀ Light')
        : (TRANSLATIONS[lang]['theme.dark']  || '🌙 Dark');
    });
  },
  init() { this.apply(this.get()); this.updateButtons(this.get()); }
};

// ═══════════════════════════════════════════════════════════════
// LANGUAGE SYSTEM
// ═══════════════════════════════════════════════════════════════
const LangManager = {
  KEY: 'eduadapt_lang',
  get() { return localStorage.getItem(this.KEY) || 'en'; },
  set(l) { localStorage.setItem(this.KEY, l); this.apply(l); this.updateButtons(l); ThemeManager.updateButtons(ThemeManager.get()); },
  t(key) {
    const lang = this.get();
    return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) || (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) || key;
  },
  apply(lang) {
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const val = this.t(el.getAttribute('data-i18n'));
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') el.placeholder = val;
      else el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      el.innerHTML = this.t(el.getAttribute('data-i18n-html'));
    });
  },
  toggle() { this.set(this.get() === 'en' ? 'fr' : 'en'); },
  updateButtons(lang) {
    document.querySelectorAll('.lang-switcher-btn').forEach(btn => {
      btn.textContent = TRANSLATIONS[lang]['lang.switch'] || (lang === 'en' ? '🇫🇷 Français' : '🇬🇧 English');
    });
  },
  init() { this.apply(this.get()); this.updateButtons(this.get()); }
};

// Global shortcut
window.t = (key) => LangManager.t(key);

